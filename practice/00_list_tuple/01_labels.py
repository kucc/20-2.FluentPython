
# The variables are labels, not boxes

# list example
lst_a = [1, 2, 3]
lst_b = lst_a

print('== The value of lst_a, lst_b ==')
print('lst_a: ', lst_a)
print('lst_b: ', lst_b)
print('equality(==): ', lst_a == lst_b)
print('identity(is): ', lst_a is lst_b, '\n')


# tuple example
tup_a = (1, 2, 3)
tup_b = tup_a

print('== The value of tup_a, tup_b ==')
print('tup_a: ', tup_a)
print('tup_b: ', tup_b)
print('equality(==): ', tup_a == tup_b)
print('identity(is): ', tup_a is tup_b, '\n')
